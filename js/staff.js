const grid = document.getElementById("staffGrid");
const principalsGrid = document.getElementById("principalsGrid");

const modal = document.getElementById("modal");
const modalImg = document.getElementById("modalImg");
const modalName = document.getElementById("modalName");
const modalRole = document.getElementById("modalRole");
const modalDept = document.getElementById("modalDept");
const modalEmail = document.getElementById("modalEmail");
const closeModal = document.getElementById("closeModal");

// Principals (first 3 staff members)
const principals = staff.slice(0, 3);

function renderPrincipals() {
    principalsGrid.innerHTML = "";

    principals.forEach(person => {
        const card = document.createElement("div");
        card.classList.add("staff-card");

        card.innerHTML = `
            <img src="${person.img}">
            <h3>${person.name}</h3>
            <p>${person.role}</p>
        `;

        card.addEventListener("click", () => {
            modal.style.display = "flex";
            modal.classList.add("open");
            modalImg.src = person.img;
            modalName.innerText = person.name;
            modalRole.innerText = person.role;
            modalDept.style.display = "none";
            modalEmail.style.display = "none";
        });

        principalsGrid.appendChild(card);
    });
}

function renderStaff(filter = "all") {
    grid.innerHTML = "";

    staff.forEach(person => {
        // Skip principals
        if (person.name.includes("Teddy Feria") || person.name.includes("Ricky Gure") || person.name.includes("Martha Mago")) return;
        
        if (filter !== "all" && person.category !== filter) return;

        const card = document.createElement("div");
        card.classList.add("staff-card");
        card.classList.add("staff-card-no-image");

        card.innerHTML = `
            <h3>${person.name}</h3>
            <p>${person.dept}</p>
        `;

        grid.appendChild(card);
    });
}

renderPrincipals();
renderStaff();

document.querySelectorAll(".filter-buttons button").forEach(btn => {
    btn.addEventListener("click", () => {
        const activeBtn = document.querySelector(".filter-buttons .active");
        if (activeBtn && activeBtn !== btn) {
            activeBtn.classList.remove("active");
        }
        btn.classList.add("active");
        renderStaff(btn.dataset.filter);
    });
});

closeModal.addEventListener("click", () => {
    modal.classList.remove("open");
    modal.style.display = "none";
});

window.addEventListener("click", e => {
    if (e.target === modal) {
        modal.classList.remove("open");
        modal.style.display = "none";
    }
});